CREATE TABLE MSGBOXES (
  MSGBOXID VARCHAR(100) NOT NULL DEFAULT '',
  PRIMARY KEY  (MSGBOXID)
); 

CREATE TABLE MSGBOX (
  ID INTEGER NOT NULL GENERATED BY DEFAULT AS IDENTITY,
  CONTENT BLOB,
  MSGBOXID VARCHAR(100) NOT NULL DEFAULT '',
  MESSAGEID VARCHAR(100) DEFAULT '',
  SOAPACTION VARCHAR(100) DEFAULT '',
  TIME TIMESTAMP ,
  PRIMARY KEY  (ID))
;
