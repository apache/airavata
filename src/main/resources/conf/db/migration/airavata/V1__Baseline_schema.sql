-- Note: Uses prefix lengths to fit within MySQL's 3072-byte key limit
CREATE TABLE IF NOT EXISTS SHARING (
    PERMISSION_TYPE_ID VARCHAR(255) NOT NULL,
    ENTITY_ID VARCHAR(255) NOT NULL,
    GROUP_ID VARCHAR(255) NOT NULL,
    INHERITED_PARENT_ID VARCHAR(255) NOT NULL,
    DOMAIN_ID VARCHAR(255) NOT NULL,
    SHARING_TYPE VARCHAR(255) NOT NULL,
    CREATED_TIME BIGINT NOT NULL,
    UPDATED_TIME BIGINT NOT NULL,
    PRIMARY KEY (PERMISSION_TYPE_ID(60), ENTITY_ID(60), GROUP_ID(60), INHERITED_PARENT_ID(60), DOMAIN_ID(60))
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4 COLLATE=utf8mb4_unicode_ci;

-- ============================================================================
-- CREDENTIAL STORE ENTITIES
-- ============================================================================

-- CREDENTIALS - Stores SSH keys and other credentials
-- Entity uses composite PK: (GATEWAY_ID, TOKEN_ID)
-- The USER_ID field tracks who owns/created the credential
CREATE TABLE IF NOT EXISTS CREDENTIALS (
    GATEWAY_ID VARCHAR(256) NOT NULL,
    TOKEN_ID VARCHAR(256) NOT NULL,
    CREDENTIAL LONGBLOB NOT NULL,
    USER_ID VARCHAR(256) NOT NULL,
    DESCRIPTION VARCHAR(500),
    TIME_PERSISTED TIMESTAMP NOT NULL DEFAULT CURRENT_TIMESTAMP,
    PRIMARY KEY (GATEWAY_ID(128), TOKEN_ID(128))
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4 COLLATE=utf8mb4_unicode_ci;

CREATE INDEX IF NOT EXISTS idx_credentials_user ON CREDENTIALS(USER_ID);
CREATE INDEX IF NOT EXISTS idx_credentials_gateway ON CREDENTIALS(GATEWAY_ID);

-- RESOURCE_ACCESS - Explicit mapping of user/group to resource with credential
CREATE TABLE IF NOT EXISTS RESOURCE_ACCESS (
    ID BIGINT AUTO_INCREMENT,
    RESOURCE_TYPE VARCHAR(50) NOT NULL,
    RESOURCE_ID VARCHAR(255) NOT NULL,
    OWNER_ID VARCHAR(512) NOT NULL,
    OWNER_TYPE VARCHAR(50) NOT NULL,
    GATEWAY_ID VARCHAR(255) NOT NULL,
    CREDENTIAL_TOKEN VARCHAR(256) NOT NULL,
    LOGIN_USERNAME VARCHAR(255),
    ENABLED BOOLEAN NOT NULL DEFAULT TRUE,
    DESCRIPTION VARCHAR(500),
    CREATION_TIME TIMESTAMP NOT NULL DEFAULT CURRENT_TIMESTAMP,
    UPDATE_TIME TIMESTAMP NOT NULL DEFAULT CURRENT_TIMESTAMP ON UPDATE CURRENT_TIMESTAMP,
    PRIMARY KEY (ID),
    UNIQUE KEY uk_resource_access (RESOURCE_TYPE, RESOURCE_ID(100), OWNER_ID(200), OWNER_TYPE),
    INDEX idx_res_access_owner (OWNER_ID, OWNER_TYPE),
    INDEX idx_res_access_resource (RESOURCE_TYPE, RESOURCE_ID),
    INDEX idx_res_access_credential (CREDENTIAL_TOKEN),
    INDEX idx_res_access_gateway (GATEWAY_ID),
    INDEX idx_res_access_enabled (ENABLED)
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4 COLLATE=utf8mb4_unicode_ci;


