-- RESOURCE_PREFERENCE - Unified key-value preferences for all resource types
--
-- This table stores hierarchical preferences with GATEWAY > GROUP > USER precedence.
-- For each key, the most specific level (USER) wins over less specific levels (GROUP, GATEWAY).
--
-- Supported RESOURCE_TYPEs:
--   COMPUTE      - Compute resource preferences (Resource ID: computeResourceId)
--   STORAGE      - Storage resource preferences (Resource ID: storageResourceId)
--   PROFILE      - Profile-level metadata (Resource ID: profileId)
--   BATCH_QUEUE  - Queue policies and limits (Resource ID: computeResourceId:queueName)
--   APPLICATION  - Application defaults and settings (Resource ID: applicationInterfaceId)
--   GATEWAY      - Gateway-level configuration (Resource ID: gatewayId)
--   SYSTEM       - System-wide settings (Resource ID: "GLOBAL" or gatewayId for overrides)
--
-- OWNER_ID format by PREFERENCE_LEVEL:
--   GATEWAY: gatewayId
--   GROUP: groupResourceProfileId
--   USER: userId@gatewayId (airavataInternalUserId)
--
-- Entity uses composite PK: (RESOURCE_TYPE, RESOURCE_ID, OWNER_ID, PREFERENCE_LEVEL, PREFERENCE_KEY)
-- Note: Uses prefix lengths to fit within MySQL's 3072-byte key limit
CREATE TABLE IF NOT EXISTS RESOURCE_PREFERENCE (
    RESOURCE_TYPE VARCHAR(50) NOT NULL,
    RESOURCE_ID VARCHAR(255) NOT NULL,
    OWNER_ID VARCHAR(512) NOT NULL,
    PREFERENCE_LEVEL VARCHAR(50) NOT NULL,
    PREFERENCE_KEY VARCHAR(255) NOT NULL,
    PREFERENCE_VALUE LONGTEXT,
    VALUE_TYPE VARCHAR(50) NOT NULL,
    CREATION_TIME TIMESTAMP NOT NULL DEFAULT CURRENT_TIMESTAMP,
    UPDATE_TIME TIMESTAMP NOT NULL DEFAULT CURRENT_TIMESTAMP ON UPDATE CURRENT_TIMESTAMP,
    PRIMARY KEY (RESOURCE_TYPE, RESOURCE_ID(100), OWNER_ID(100), PREFERENCE_LEVEL, PREFERENCE_KEY(100)),
    INDEX idx_res_pref_resource (RESOURCE_TYPE, RESOURCE_ID),
    INDEX idx_res_pref_owner (OWNER_ID),
    INDEX idx_res_pref_level (PREFERENCE_LEVEL),
    INDEX idx_res_pref_lookup (RESOURCE_TYPE, RESOURCE_ID, PREFERENCE_LEVEL, PREFERENCE_KEY)
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4 COLLATE=utf8mb4_unicode_ci;

