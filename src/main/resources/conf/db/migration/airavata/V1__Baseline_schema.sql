-- ============================================================================
-- VIEW FOR LATEST EXPERIMENT STATUS
-- ============================================================================

CREATE OR REPLACE VIEW LATEST_EXPERIMENT_STATUS AS
SELECT ES1.PARENT_ID AS EXPERIMENT_ID,
       ES1.STATE,
       ES1.TIME_OF_STATE_CHANGE,
       ES1.REASON
FROM STATUS ES1
WHERE ES1.PARENT_TYPE = 'EXPERIMENT'
  AND ES1.SEQUENCE_NUM = (
      SELECT MAX(ES2.SEQUENCE_NUM)
      FROM STATUS ES2
      WHERE ES2.PARENT_ID = ES1.PARENT_ID
        AND ES2.PARENT_TYPE = 'EXPERIMENT'
  );

