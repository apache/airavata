-- GATEWAY - Unified gateway entity (formerly split across profile and experiment catalog)
CREATE TABLE IF NOT EXISTS GATEWAY (
    AIRAVATA_INTERNAL_GATEWAY_ID VARCHAR(255) NOT NULL,
    GATEWAY_ID VARCHAR(255) UNIQUE,
    GATEWAY_NAME VARCHAR(255),
    GATEWAY_DOMAIN VARCHAR(255),
    EMAIL_ADDRESS VARCHAR(255),
    GATEWAY_APPROVAL_STATUS VARCHAR(50),
    GATEWAY_ACRONYM VARCHAR(255),
    GATEWAY_URL VARCHAR(255),
    GATEWAY_PUBLIC_ABSTRACT TEXT,
    GATEWAY_REVIEW_PROPOSAL_DESCRIPTION TEXT,
    GATEWAY_ADMIN_FIRST_NAME VARCHAR(255),
    GATEWAY_ADMIN_LAST_NAME VARCHAR(255),
    GATEWAY_ADMIN_EMAIL VARCHAR(255),
    DECLINED_REASON TEXT,
    REQUEST_CREATION_TIME TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
    REQUESTER_USERNAME VARCHAR(255),
    -- Domain fields (merged from sharing DOMAIN table)
    DOMAIN_DESCRIPTION TEXT,
    DOMAIN_CREATED_TIME BIGINT,
    LAST_UPDATED_TIME BIGINT,
    INITIAL_USER_GROUP_ID VARCHAR(255),
    PRIMARY KEY (AIRAVATA_INTERNAL_GATEWAY_ID)
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4 COLLATE=utf8mb4_unicode_ci;

CREATE INDEX IF NOT EXISTS idx_gateway_id ON GATEWAY(GATEWAY_ID);
CREATE INDEX IF NOT EXISTS idx_gateway_approval_status ON GATEWAY(GATEWAY_APPROVAL_STATUS);

-- AIRAVATA_USER - User entity storing only OpenID Connect scope fields
-- Additional user profile data should be fetched from the identity provider (Keycloak)
CREATE TABLE IF NOT EXISTS AIRAVATA_USER (
    -- Internal composite ID: userId@gatewayId
    AIRAVATA_INTERNAL_USER_ID VARCHAR(512) NOT NULL,
    -- OIDC 'openid' scope
    SUB VARCHAR(255) NOT NULL,                    -- OIDC subject identifier (user ID)
    GATEWAY_ID VARCHAR(255) NOT NULL,             -- Airavata gateway context
    -- OIDC 'profile' scope
    GIVEN_NAME VARCHAR(255),                      -- given_name claim
    FAMILY_NAME VARCHAR(255),                     -- family_name claim
    PREFERRED_USERNAME VARCHAR(255),              -- preferred_username claim
    PICTURE_URL VARCHAR(1024),                    -- picture claim (URL)
    ZONEINFO VARCHAR(100),                        -- zoneinfo claim (timezone)
    LOCALE VARCHAR(50),                           -- locale claim
    -- OIDC 'email' scope
    EMAIL VARCHAR(255),                           -- email claim
    EMAIL_VERIFIED BOOLEAN,                       -- email_verified claim
    -- Timestamps
    CREATED_AT TIMESTAMP NOT NULL DEFAULT CURRENT_TIMESTAMP,
    UPDATED_AT BIGINT,                            -- OIDC updated_at claim (seconds since epoch)
    PRIMARY KEY (AIRAVATA_INTERNAL_USER_ID)
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4 COLLATE=utf8mb4_unicode_ci;

CREATE INDEX IF NOT EXISTS idx_user_sub ON AIRAVATA_USER(SUB);
CREATE INDEX IF NOT EXISTS idx_user_gateway_id ON AIRAVATA_USER(GATEWAY_ID);
CREATE INDEX IF NOT EXISTS idx_user_sub_gateway_id ON AIRAVATA_USER(SUB, GATEWAY_ID);
CREATE INDEX IF NOT EXISTS idx_user_email ON AIRAVATA_USER(EMAIL);

